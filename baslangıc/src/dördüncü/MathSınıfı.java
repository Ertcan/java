package dördüncü;

public class MathSýnýfý {
public static void main(String[] args) {
	//Math Sýnýfý Metotlarý
	//Math.ceil(x) => bir büyük tam sayýya yuvarlama yapar.		
	//Math.cos(x) => cos deðerini döner.		
	//Math.sin(x) => sin deðerini döner.		
	//Math.floor(x) => küçük tam sayýya yuvarlar.
	//Math.max(x,y) => büyük sayýyý verir.
	//Math.min(x,y) => küçük sayýyý verir.
	//Math.pow(3,5) => 3^5 üs alýr.
	//Math.random() => 1 ve 0 arasýnda rastgele sayý seçer.
	//Math.round(x) => yakýn olan tam sayýya böler.
	//Math.abs(x) => mutlak deðerini alýr.
	//Math.sqrt(x) => karekökünü alýr.
	System.out.println(Math.ceil(4.1));
	System.out.println(Math.cos(60));
	System.out.println(Math.sin(30));
	System.out.println(Math.floor(1.9));
	System.out.println(Math.max(3.1, 3.2));
	System.out.println(Math.min(3.2, 3.1));
	System.out.println(Math.pow(10, 2));
	System.out.println(Math.random());
	System.out.println(Math.round(1.5));
	System.out.println(Math.abs(-5));
	System.out.println(Math.sqrt(9));
}
}
