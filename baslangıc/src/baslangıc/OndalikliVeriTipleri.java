package baslangýc;

public class OndalikliVeriTipleri {

	public static void main(String[] args) {
		float floatDegeri= 34.5f; // 34.5f veya 34.5F olarak tanýmlanabilir bide 34 olarakda tanýmlanabilir.
		System.out.println(floatDegeri);
		double doubleDegeri = 347.54d;
		System.out.println(doubleDegeri);
		double sayi1 = 65;
		System.out.println(sayi1);
		sayi1 = 66.66;
		System.out.println(sayi1);
	}

}
